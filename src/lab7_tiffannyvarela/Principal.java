    private void btn_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entrarActionPerformed
        // TODO add your handling code here:
        
        try {
            for (int i = 0; i < personas.size(); i++) {
            if (txt_user.getText().equals(((usuario)personas.get(i)).getUsuario())&&jpass_contra.getText().equals(((usuario)personas.get(i)).getContra())) {
                JOptionPane.showMessageDialog(entrar, "Bienvenido");
                jm_usuarios.setEnabled(true);
                entrar.setVisible(false); 
            }
            
            if (txt_user.getText().equals(((artista)personas.get(i)).getUsuario())&&jpass_contra.getText().equals(((artista)personas.get(i)).getContra())) {
                JOptionPane.showMessageDialog(entrar, "Bienvenido");
                jm_artista.setEnabled(true);
                entrar.setVisible(false);
            }
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(entrar, "Usuario y contrasena no validos");
        }
        finally{
            txt_user.setText("");
            jpass_contra.setText("");
            
        }
    }//GEN-LAST:event_btn_entrarActionPerformed

