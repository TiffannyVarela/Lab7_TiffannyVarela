    private void cmb_listaplaylistItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_listaplaylistItemStateChanged
        // TODO add your handling code here:
        DefaultListModel modelo = (DefaultListModel) jl_cancionesplaylist.getModel();
        if (evt.getStateChange() ==1 ) {
            playlist pl = (playlist) cmb_listaplaylist.getSelectedItem();
            
            for (int i = 0; i < pl.getCanciones().size(); i++) {
                modelo.add(i, pl.getCanciones().get(i));
            }
            jl_cancionesplaylist.setModel(modelo);
        }
    }//GEN-LAST:event_cmb_listaplaylistItemStateChanged

